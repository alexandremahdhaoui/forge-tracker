	m.HandleFunc("DELETE "+options.BaseURL+"/api/v1/tracking-sets/{ts}/metaplans/{id}", wrapper.DeleteMetaPlan)
	m.HandleFunc("GET "+options.BaseURL+"/api/v1/tracking-sets/{ts}/metaplans/{id}", wrapper.GetMetaPlan)
